ft_list = ["Hello", "World!"]  #list is a collection of items that are ordered and mutable (changeable)
ft_tuple = ("Hello", "Morocco!") # similar to a list, but it is immutable (unchangeable)
ft_set = {"Hello": "Benguerir!"} # set is an unordered collection of unique elements, similar to set in cpp [do not allow duplicates]
ft_dict = {"Hello" : "1337!"} # dictionary is a collection of key-value pairs, similar to maps in cpp 

print(ft_list)
print(ft_tuple)
print(ft_set)
print(ft_dict)

